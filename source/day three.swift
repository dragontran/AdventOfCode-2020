// part one
func searchTrees(input: String, right: Int, down: Int) -> Int {
    let inputRows = input.components(separatedBy: "\n")
    var rowIndex = down
    var columnIndex = right
    var treeCount = 0
    
    while rowIndex < inputRows.count {
        if inputRows[rowIndex][columnIndex] == Character("#") {
            treeCount += 1
        }
        rowIndex += down
        columnIndex = (columnIndex + right) % inputRows[0].count
    }
    
    return treeCount
}

